public class House {
    private String baisic;
    private String wall;
    private String roofed;

    public String getBaisic() {
        return baisic;
    }

    public void setBaisic(String baisic) {
        this.baisic = baisic;
    }

    public String getWall() {
        return wall;
    }

    public void setWall(String wall) {
        this.wall = wall;
    }

    public String getRoofed() {
        return roofed;
    }

    public void setRoofed(String roofed) {
        this.roofed = roofed;
    }
}
